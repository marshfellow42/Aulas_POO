package br.edu.figespaciais;

public class FigGeoEspaciais {
	
	protected double areaLateral;
	protected double areaBase;
	protected double areaTotal;
	protected double areaVolume;
	
	public double getAreaLateral() {
		return areaLateral;
	}
	
	public void setAreaLateral(double areaLateral) {
		this.areaLateral = areaLateral;
	}
	
	public double getAreaBase() {
		return areaBase;
	}
	
	public void setAreaBase(double areaBase) {
		this.areaBase = areaBase;
	}
	
	public double getAreaTotal() {
		return areaTotal;
	}
	
	public void setAreaTotal(double areaTotal) {
		this.areaTotal = areaTotal;
	}
	
	public double getAreaVolume() {
		return areaVolume;
	}
	
	public void setAreaVolume(double areaVolume) {
		this.areaVolume = areaVolume;
	}
}
